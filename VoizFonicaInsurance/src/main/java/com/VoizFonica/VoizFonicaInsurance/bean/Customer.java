package com.VoizFonica.VoizFonicaInsurance.bean;

public class Customer {

	String vechileNumber;
	String vechileType;
	Integer InsurancePeriod;
	Long aadharNumber;
	Long mobileNumber;
	String registeredDate;
	public String getVechileNumber() {
		return vechileNumber;
	}
	public void setVechileNumber(String vechileNumber) {
		this.vechileNumber = vechileNumber;
	}
	public String getVechileType() {
		return vechileType;
	}
	public void setVechileType(String vechileType) {
		this.vechileType = vechileType;
	}
	public Integer getInsurancePeriod() {
		return InsurancePeriod;
	}
	public void setInsurancePeriod(Integer insurancePeriod) {
		InsurancePeriod = insurancePeriod;
	}
	public Long getAadharNumber() {
		return aadharNumber;
	}
	public void setAadharNumber(Long aadharNumber) {
		this.aadharNumber = aadharNumber;
	}
	public Long getMobileNumber() {
		return mobileNumber;
	}
	public void setMobileNumber(Long mobileNumber) {
		this.mobileNumber = mobileNumber;
	}
	public String getRegisteredDate() {
		return registeredDate;
	}
	public void setRegisteredDate(String registeredDate) {
		this.registeredDate = registeredDate;
	}
	
	
	
}
